def generate_normalized_test_data():
    """生成规范的测试数据集"""
    test_cases = []

    for intent, texts in TEST_DATA_MAP.items():
        for text in texts:
            test_cases.append({
                "original_text": text,
                "expected_intent": intent,
            })

    return test_cases


def save_test_data(data, filepath: str):
    """保存测试数据到文件"""
    with open(filepath, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)


